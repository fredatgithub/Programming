print("Can show if if a variable is true or false:")
x = true
print(x)
print("\n") --Creates a new line

print("The not operator can be placed before a boolean to reverse it:")
print(not x)
print(not false)
print("\n")


print("Used to show logical events with '==' or '~=':")
print(1 == 0)
print(1 == 1)
print(1 ~= 2) --~= is "not true"
print(true ~= false)
print("\n")
